void model_init(){
    conv_1_data = init_conv_1_data();
    conv_2_data = init_conv_2_data();
    dense_1_data = init_dense_1_data();
    dense_2_data = init_dense_2_data();

}

void model_predict(data3d_t input, data1d_t * output){
  
    data3d_t output0;

    //*************** LAYER 0 ***************//
    // Layer name: conv_1
    // convert image for first EmbedIA Conv2d layer
    image_adapt_layer(input, &output0);
    input = output0;

    conv2d_layer(conv_1_data, input, &output0);
    // Activation layer for conv_1
    leakyrelu_activation(output0.data, 512, FL2FX(0.30000001192092896));
    // Debug function for layer conv_1
    print_data3d_t("conv_1", output0);

    input = output0;

    //*************** LAYER 1 ***************//
    // Layer name: maxp_1

    static const pooling2d_layer_t maxp_1_data = { 2, 2 };
    max_pooling2d_layer(maxp_1_data, input, &output0);
    // Debug function for layer maxp_1
    print_data3d_t("maxp_1", output0);

    input = output0;

    //*************** LAYER 2 ***************//
    // Layer name: conv_2
   conv2d_layer(conv_2_data, input, &output0);
    // Activation layer for conv_2
    leakyrelu_activation(output0.data, 64, FL2FX(0.30000001192092896));
    // Debug function for layer conv_2
    print_data3d_t("conv_2", output0);

    input = output0;

    //*************** LAYER 3 ***************//
    // Layer name: maxp_2

    static const pooling2d_layer_t maxp_2_data = { 2, 2 };
    max_pooling2d_layer(maxp_2_data, input, &output0);
    // Debug function for layer maxp_2
    print_data3d_t("maxp_2", output0);

    input = output0;
    data1d_t output1;

    //*************** LAYER 4 ***************//
    // Layer name: flatten_4
    flatten3d_layer(input, &output1);
    // Debug function for layer flatten_4
    print_data1d_t("flatten_4", output1);
    data1d_t input1;

    input1 = output1;

    //*************** LAYER 5 ***************//
    // Layer name: dense_1
    dense_layer(dense_1_data, input1, &output1);
    // Activation layer for dense_1
    leakyrelu_activation(output1.data, 20, FL2FX(0.30000001192092896));
    // Debug function for layer dense_1
    print_data1d_t("dense_1", output1);

    input1 = output1;

    //*************** LAYER 6 ***************//
    // Layer name: dense_2
    dense_layer(dense_2_data, input1, &output1);
    // Activation layer for dense_2
    // remove softmax activation to reduce memory size and time
    // softmax_activation(output1.data, 10);
    // Debug function for layer dense_2
    print_data1d_t("dense_2", output1);


    *output = output1;

}

int model_predict_class(data3d_t input, data1d_t * results){
  
   
    model_predict(input, results);
    
        return argmax(*results); 
    //return argmax(data1d_t);

}

