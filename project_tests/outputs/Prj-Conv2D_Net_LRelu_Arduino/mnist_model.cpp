void model_predict(data3d_t input, data1d_t * output){
  
    data3d_t output0;

    //*************** LAYER 0 ***************//
    // Layer name: conv2d_4
    // convert image for first EmbedIA Conv2d layer
    image_adapt_layer(input, &output0);
    input = output0;

    conv2d_layer(conv2d_4_data, input, &output0);
    // Activation layer for conv2d_4
    leakyrelu_activation(output0.data, 1210, FL2FX(0.30000001192092896));
    // Debug function for layer conv2d_4
    print_data3d_t("conv2d_4", output0);

    input = output0;

    //*************** LAYER 1 ***************//
    // Layer name: max_pooling2d_4

    static const pooling2d_layer_t max_pooling2d_4_data = { 2, 2 };
    max_pooling2d_layer(max_pooling2d_4_data, input, &output0);
    // Debug function for layer max_pooling2d_4
    print_data3d_t("max_pooling2d_4", output0);

    input = output0;

    //*************** LAYER 2 ***************//
    // Layer name: conv2d_5
   conv2d_layer(conv2d_5_data, input, &output0);
    // Activation layer for conv2d_5
    leakyrelu_activation(output0.data, 240, FL2FX(0.30000001192092896));
    // Debug function for layer conv2d_5
    print_data3d_t("conv2d_5", output0);

    input = output0;

    //*************** LAYER 3 ***************//
    // Layer name: max_pooling2d_5

    static const pooling2d_layer_t max_pooling2d_5_data = { 2, 2 };
    max_pooling2d_layer(max_pooling2d_5_data, input, &output0);
    // Debug function for layer max_pooling2d_5
    print_data3d_t("max_pooling2d_5", output0);

    input = output0;
    data1d_t output1;

    //*************** LAYER 4 ***************//
    // Layer name: flatten_2
    flatten3d_layer(input, &output1);
    // Debug function for layer flatten_2
    print_data1d_t("flatten_2", output1);
    data1d_t input1;

    input1 = output1;

    //*************** LAYER 5 ***************//
    // Layer name: dense_2
    dense_layer(dense_2_data, input1, &output1);
    // Activation layer for dense_2
    
    // Debug function for layer dense_2
    print_data1d_t("dense_2", output1);

    //*************** LAYER 6 ***************//
    // Layer name: leaky_re_lu_6
    leakyrelu_activation(output1.data, 16, FL2FX(0.30000001192092896));    // Debug function for layer leaky_re_lu_6
    print_data1d_t("leaky_re_lu_6", output1);

    input1 = output1;

    //*************** LAYER 7 ***************//
    // Layer name: dense_3
    dense_layer(dense_3_data, input1, &output1);
    // Activation layer for dense_3
    softmax_activation(output1.data, 10);
    // Debug function for layer dense_3
    print_data1d_t("dense_3", output1);


    *output = output1;

}

int model_predict_class(data3d_t input, data1d_t * results){
  
   
    model_predict(input, results);
    
        return argmax(*results); 
    //return argmax(data1d_t);

}

