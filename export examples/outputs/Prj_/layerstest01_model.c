    avg_pooling2d_layer(avg_pool_3_data, input, &output0);
    // Debug function for layer avg_pool_3
    print_data3d_t("avg_pool_3", output0);
    
    //******************** LAYER 14 *******************//
    // Layer name: flatten_4
    input = output0;
    data1d_t output1;
    flatten3d_layer(input, &output1);
    // Debug function for layer flatten_4
    print_data1d_t("flatten_4", output1);
    
    //******************** LAYER 15 *******************//
    // Layer name: dense_4
