void model_predict(data3d_t input, data1d_t * output){
  
    prepare_buffers();
    
    //******************** LAYER 0 *******************//
    // Layer name: conv_2d_1
    data3d_t output0;
    conv2d_layer(conv_2d_1_data, input, &output0);
    // Debug function for layer conv_2d_1
    print_data3d_t("conv_2d_1", output0);
    
    //******************** LAYER 1 *******************//
    // Layer name: leaky_relu_1
    leakyrelu_activation(output0.data, 800, 9830);
    // Debug function for layer leaky_relu_1
    print_data3d_t("leaky_relu_1", output0);
    
    //******************** LAYER 2 *******************//
    // Layer name: max_pool_2d_1
    input = output0;
    static const pooling2d_layer_t max_pool_2d_1_data = { 2, 2 };
    max_pooling2d_layer(max_pool_2d_1_data, input, &output0);
    // Debug function for layer max_pool_2d_1
    print_data3d_t("max_pool_2d_1", output0);
    
    //******************** LAYER 3 *******************//
    // Layer name: conv_2d_2
    input = output0;
    conv2d_layer(conv_2d_2_data, input, &output0);
    // Debug function for layer conv_2d_2
    print_data3d_t("conv_2d_2", output0);
    
    //******************** LAYER 4 *******************//
    // Layer name: leaky_relu_2
    leakyrelu_activation(output0.data, 144, 9830);
    // Debug function for layer leaky_relu_2
    print_data3d_t("leaky_relu_2", output0);
    
    //******************** LAYER 5 *******************//
    // Layer name: max_pool_2d_2
    input = output0;
    static const pooling2d_layer_t max_pool_2d_2_data = { 2, 2 };
    max_pooling2d_layer(max_pool_2d_2_data, input, &output0);
    // Debug function for layer max_pool_2d_2
    print_data3d_t("max_pool_2d_2", output0);
    
    //******************** LAYER 6 *******************//
    // Layer name: reshape_1
    input = output0;
    data1d_t output1;
    flatten3d_layer(input, &output1);
    // Debug function for layer reshape_1
    print_data1d_t("reshape_1", output1);
    
    //******************** LAYER 7 *******************//
    // Layer name: fully_connected_1
    data1d_t input1;
    input1 = output1;
    dense_layer(fully_connected_1_data, input1, &output1);
    
    // Debug function for layer fully_connected_1
    print_data1d_t("fully_connected_1", output1);
    
    //******************** LAYER 8 *******************//
    // Layer name: leaky_relu_3
    leakyrelu_activation(output1.data, 16, 9830);
    // Debug function for layer leaky_relu_3
    print_data1d_t("leaky_relu_3", output1);
    
    //******************** LAYER 9 *******************//
    // Layer name: fully_connected_2
    input1 = output1;
    dense_layer(fully_connected_2_data, input1, &output1);
    
    // Debug function for layer fully_connected_2
    print_data1d_t("fully_connected_2", output1);
    
    //******************** LAYER 10 *******************//
    // Layer name: softmax_1
    softmax_activation(output1.data, 10);
    softmax_activation(output1.data, 10);
    // Debug function for layer softmax_1
    print_data1d_t("softmax_1", output1);
    

    *output = output1;

}

int model_predict_class(data3d_t input, data1d_t * results){
  
   
    model_predict(input, results);
    
    return argmax(*results);
    //return argmax(data1d_t);

}

