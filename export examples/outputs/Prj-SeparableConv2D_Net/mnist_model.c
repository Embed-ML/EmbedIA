void model_init(){
    separable_conv2d_data = init_separable_conv2d_data();
    separable_conv2d_1_data = init_separable_conv2d_1_data();
    dense_data = init_dense_data();
    dense_1_data = init_dense_1_data();

}

void model_predict(data3d_t input, data1d_t * output){
  
    prepare_buffers();
    
    //******************** LAYER 0 *******************//
    // Layer name: separable_conv2d
    data3d_t output0;
    separable_conv2d_layer(separable_conv2d_data, input, &output0);
    // Activation layer for separable_conv2d
    relu_activation(output0.data, 1152);
    // Debug function for layer separable_conv2d
    print_data3d_t("separable_conv2d", output0);
    
    //******************** LAYER 1 *******************//
    // Layer name: separable_conv2d_1
    input = output0;
    separable_conv2d_layer(separable_conv2d_1_data, input, &output0);
    // Activation layer for separable_conv2d_1
    relu_activation(output0.data, 800);
    // Debug function for layer separable_conv2d_1
    print_data3d_t("separable_conv2d_1", output0);
    
    //******************** LAYER 2 *******************//
    // Layer name: max_pooling2d
    input = output0;
    static const pooling2d_layer_t max_pooling2d_data = { 3, 3 };
    max_pooling2d_layer(max_pooling2d_data, input, &output0);
    // Debug function for layer max_pooling2d
    print_data3d_t("max_pooling2d", output0);
    
    //******************** LAYER 3 *******************//
    // Layer name: flatten
    input = output0;
    data1d_t output1;
    flatten3d_layer(input, &output1);
    // Debug function for layer flatten
    print_data1d_t("flatten", output1);
    
    //******************** LAYER 4 *******************//
    // Layer name: dense
    data1d_t input1;
    input1 = output1;
    dense_layer(dense_data, input1, &output1);
    
    // Activation layer for dense
    relu_activation(output1.data, 16);
    // Debug function for layer dense
    print_data1d_t("dense", output1);
    
    //******************** LAYER 5 *******************//
    // Layer name: dense_1
    input1 = output1;
    dense_layer(dense_1_data, input1, &output1);
    
    // Activation layer for dense_1
    softmax_activation(output1.data, 10);
    // Debug function for layer dense_1
    print_data1d_t("dense_1", output1);
    

    *output = output1;

}

int model_predict_class(data3d_t input, data1d_t * results){
  
   
    model_predict(input, results);
    
    return argmax(*results);
    //return argmax(data1d_t);

}

