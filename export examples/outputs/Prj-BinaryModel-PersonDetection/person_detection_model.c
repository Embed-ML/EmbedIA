void model_predict(data3d_t input, data1d_t * output){
  
    data3d_t output0;

    //*************** LAYER 0 ***************//
    // Layer name: quant_conv2d_3
    // convert image for first EmbedIA Conv2d layer
    image_adapt_layer(input, &output0);
    input = output0;

     conv2d_layer(quant_conv2d_3_data, input, &output0);
        // Activation layer for quant_conv2d_3
    relu_activation(output0.data, 135424);

    //*************** LAYER 1 ***************//
    // Layer name: batch_normalization_5
    batch_normalization3d_layer(batch_normalization_5_data, &output0);

    input = output0;

    //*************** LAYER 2 ***************//
    // Layer name: max_pooling2d_2

    static const pooling2d_layer_t max_pooling2d_2_data = { 4, 4 };
    max_pooling2d_layer(max_pooling2d_2_data, input, &output0);

    //*************** LAYER 3 ***************//
    // Layer name: batch_normalization_6
    batch_normalization3d_layer(batch_normalization_6_data, &output0);

    input = output0;

    //*************** LAYER 4 ***************//
    // Layer name: quant_conv2d_4
    quantconv2d_layer(quant_conv2d_4_data, input, &output0);
        // Activation layer for quant_conv2d_4
    relu_activation(output0.data, 25600);

    input = output0;

    //*************** LAYER 5 ***************//
    // Layer name: max_pooling2d_3

    static const pooling2d_layer_t max_pooling2d_3_data = { 3, 3 };
    max_pooling2d_layer(max_pooling2d_3_data, input, &output0);

    //*************** LAYER 6 ***************//
    // Layer name: batch_normalization_7
    batch_normalization3d_layer(batch_normalization_7_data, &output0);

    input = output0;

    //*************** LAYER 7 ***************//
    // Layer name: quant_conv2d_5
    quantconv2d_layer(quant_conv2d_5_data, input, &output0);
        // Activation layer for quant_conv2d_5
    relu_activation(output0.data, 1024);

    //*************** LAYER 8 ***************//
    // Layer name: batch_normalization_8
    batch_normalization3d_layer(batch_normalization_8_data, &output0);

    input = output0;
    data1d_t output1;

    //*************** LAYER 9 ***************//
    // Layer name: flatten_1
    flatten3d_layer(input, &output1);
    data1d_t input1;

    input1 = output1;

    //*************** LAYER 10 ***************//
    // Layer name: quant_dense_1
    quantdense_layer(quant_dense_1_data, input1, &output1);
        // Activation layer for quant_dense_1
    relu_activation(output1.data, 256);

    //*************** LAYER 11 ***************//
    // Layer name: batch_normalization_9
    batch_normalization1d_layer(batch_normalization_9_data, &output1);

    input1 = output1;

    //*************** LAYER 12 ***************//
    // Layer name: dense_1
    dense_layer(dense_1_data, input1, &output1);
    // Activation layer for dense_1
    softmax_activation(output1.data, 2);


    *output = output1;

}

int model_predict_class(data3d_t input, data1d_t * results){
  
   
    model_predict(input, results);
    
        return argmax(*results); 
    //return argmax(data1d_t);

}

