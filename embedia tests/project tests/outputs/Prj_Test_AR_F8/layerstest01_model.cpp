    // Layer name: flatten_4
    input = output0;
    data1d_t output1;
    flatten3d_layer(input, &output1);
    // Debug function for layer flatten_4
    print_data1d_t("flatten_4", output1);
    
    //******************** LAYER 12 *******************//
    // Layer name: dense_4
