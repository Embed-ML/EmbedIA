    // Debug function for layer max_pool_1
    print_data3d_t("max_pool_1", output0);
    
    //******************** LAYER 5 *******************//
    // Layer name: conv2_d_2
    input = output0;
    conv2d_layer(conv2_d_2_data, input, &output0);
    // Debug function for layer conv2_d_2
    print_data3d_t("conv2_d_2", output0);
    
