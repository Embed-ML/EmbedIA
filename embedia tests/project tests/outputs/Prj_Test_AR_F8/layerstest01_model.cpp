    // Layer name: conv2_d_21
    relu_activation(output0.data, 192);
    // Debug function for layer conv2_d_21
    print_data3d_t("conv2_d_21", output0);
    
    //******************** LAYER 7 *******************//
    // Layer name: avg_pool_2
