    print_data1d_t("dense_5", output1);
    
    //******************** LAYER 15 *******************//
    // Layer name: dense_51
    
    // Debug function for layer dense_51
    print_data1d_t("dense_51", output1);
    
    //******************** LAYER 16 *******************//
    // Layer name: soft_max_6
    softmax_activation(output1.data, 10);
    // Debug function for layer soft_max_6
    print_data1d_t("soft_max_6", output1);
    

